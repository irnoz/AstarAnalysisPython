import random

def generate_maze(size):
    maze = [[0] * size for _ in range(size)]
    for i in range(size):
        for j in range(size):
            maze[i][j] = random.choice([0, 1])
    return maze

def generate_maze_zeros(size):
    maze = [[0] * size for _ in range(size)]
    for i in range(size):
        for j in range(size):
            maze[i][j] = 0
    return maze

def print_maze(maze):
    for row in maze:
        print(row)

